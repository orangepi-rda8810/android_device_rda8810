#define __TGT_AP_FLASH_LOGIC_BLOCK_SHIFT__   16

/*chip size right shift 26 bit, 64Mbytes unit*/
/*chipsize = (1 << __TGT_AP_FLASH_LOGIC_CHIP_SHIFT_GB__) * __TGT_AP_FLASH_LOGIC_CHIP_SHIFT_DIV__*/
