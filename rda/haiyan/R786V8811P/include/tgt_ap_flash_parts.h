#define __TGT_AP_FLASH_LOGIC_PAGE_SHIFT__   10

/*blocksize = (1 << __TGT_AP_FLASH_LOGIC_BLOCK_SHIFT__) * __TGT_AP_FLASH_LOGIC_BLOCK_SIZE_N_KB__*/
